package if_else;

public class NumbersignChecking_task2 {

	public static void main(String[] args) {
		//input without scanner class
		//input taken is negative
		int num=-12;
		
		//logic using loop if-elseif-else
		if(num>0) {
			System.out.print("POSITIVE");
		}
		else if(num<0){
			System.out.print("NEGATIVE");
		
		}
		else {
			System.out.print("ZERO");
		}

		//output=NEGATIVE
	}

}
